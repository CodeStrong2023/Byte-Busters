package cliclowhile;

public class ClaseCicloWhile {

    public static void main(String[] args) {

        // Clase 1 Java
        // Ciclo While se repide mientras la condicion sea verdadera

        var cont = 0;

        while (cont < 7) {
            System.out.println("Conteo = " + cont);
            cont++;
        }

    }
}
